package calculator;

public class CalculatorDemo 
{

	public static void main() 
	{
		System.out.println("This is simple CalculatorDemo program. Version - 1.0.6 ");

	}


	public static int add(int firstValue ,int secondValue ) 
	{
		return firstValue + secondValue;
	}

	public static int substract(int firstValue ,int secondValue ) 
	{
		return firstValue - secondValue;
	}

	public static int multiply(int firstValue ,int secondValue ) 
	{
		return firstValue * secondValue;
	}

	public static int devide(int firstValue ,int secondValue ) 
	{
		return firstValue/ secondValue;
	}

}
